import { createAsyncThunk } from "@reduxjs/toolkit";
import API from "../../../services/API";
import { toast } from "react-toastify";

export const userLogin = createAsyncThunk(
  "auth/login",
  
  async ({ role, email, password }, { rejectWithValue }) => {
    try {
      const { data } = await API.post("/auth/login", { role, email, password });
      //store token
      if (data.success) {
        toast.success("Loggen in successfully");

        localStorage.setItem("token", data.token);
        
        window.location.replace("/");
      }

      return data;
    } catch (error) {
      if (error.response && error.response.data.message) {
        return rejectWithValue(error.response.data.message);
      } else {
        return rejectWithValue(error.message);
      }
    }
  }
);


export const userRegister = createAsyncThunk(
  "auth/register",
  async ({
    name,
    role,
    email,
    password,
    phone,
    organisationName,
    address,
    hospitalName,
    website,
  }, { rejectWithValue ,dispatch}) => {
    try {
      const { data } = await API.post("/auth/register", {
        name,
        role,
        email,
        password,
        phone,
        organisationName,
        address,
        hospitalName,
        website,
      });

      if (data?.success) {
         toast.success("User Registered Successfully"); //successs message
         await dispatch(userLogin({role,email,password})) //from this line i automatically logged in when user is created
      }
    } catch (error) {
      console.error("Error in userRegister:", error);
      // Handle specific error cases if needed
      if (error.response && error.response.data.message) {
        return rejectWithValue(error.response.data.message);
      } else {
        return rejectWithValue(error.message);
      }
    }
  }
);

